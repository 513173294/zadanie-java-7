class Triangle {
    private String nazwa;
    private String typ;


    public String getNazwa() {
        return nazwa;
    }

    public void setNazwa(String nazwa) {
        this.nazwa = nazwa;
    }

    public String getTyp() {
        return typ;
    }

    public void setTyp(String typ) {
        this.typ = typ;
    }

    public Triangle(String nazwa, String typ) {
        this.nazwa = nazwa;
        this.typ = typ;





    }
}